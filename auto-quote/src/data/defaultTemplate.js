export function nextId() {
  return crypto.randomUUID()
}

export function todayDateStr() {
  const d = new Date()
  return `${d.getFullYear()}년 ${d.getMonth() + 1}월 ${d.getDate()}일`
}

/** 빈 견적서용 - 카테고리 1개 + 행 1개만 */
export function createBlankRows() {
  const id = nextId
  return [
    { id: id(), type: 'header', item: '1. ', spec: '', qty: '', unitPrice: '', amount: '', note: '' },
    { id: id(), type: 'row',    item: '', spec: '', qty: '', unitPrice: '', amount: '', note: '', level: 1 },
  ]
}

/** header 행의 앞 번호를 순서대로 재정렬 */
export function renumberHeaders(rows) {
  let count = 0
  return rows.map((row) => {
    if (row.type !== 'header') return row
    count++
    // "N. 텍스트" 또는 "N.텍스트" 패턴의 번호 부분만 교체
    const label = row.item.replace(/^\d+\.\s*/, '')
    return { ...row, item: `${count}. ${label}` }
  })
}
